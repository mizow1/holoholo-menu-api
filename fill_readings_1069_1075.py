def main():
    # 元のJSONを読み込む
    with open('reading_requests_1069_1075.json', 'r', encoding='utf-8') as f:
        menus = json.load(f)

    # 各メニューを処理
    for menu in menus:
        contents_id = menu['contents_id']

        # メニュー情報を追加
        if contents_id in MENU_INFO:
            menu['menu_name'] = MENU_INFO[contents_id]['menu_name']
            menu['catch'] = MENU_INFO[contents_id]['catch']
            menu['caption'] = MENU_INFO[contents_id]['caption']

        # 各鑑定文を生成
        for reading in menu['readings']:
            reading['reading_text'] = generate_reading(
                reading['item_name'],
                reading['card_name'],
                reading['card_keyword'],
                reading['card_summary'],
                reading['category_id']
            )

    # 結果を保存
    output_file = 'readings_filled_1069_1075.json'
    with open(output_file, 'w', encoding='utf-8') as f:
        json.dump(menus, f, ensure_ascii=False, indent=2)

    print(f"鑑定文入りJSONを生成しました: {output_file}")

    # サマリー
    total_readings = sum(len(m['readings']) for m in menus)
    print(f"\n生成サマリー:")
    for menu in menus:
        print(f"  - Menu {menu['contents_id']}: {len(menu['readings'])}個の鑑定文")
    print(f"  合計: {total_readings}個の鑑定文")


