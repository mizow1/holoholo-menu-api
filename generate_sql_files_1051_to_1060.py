def create_sql_file(menu_id, data):
    """SQLファイルを生成する"""
    sql_lines = []
    sql_lines.append('-- ' + '=' * 40)
    sql_lines.append(f'-- 占いメニュー: {data["name"]}')
    sql_lines.append(f'-- Contents ID: {menu_id}')
    sql_lines.append(f'-- 作成日: {datetime.now().strftime("%Y-%m-%d")}')
    sql_lines.append('-- ' + '=' * 40)
    sql_lines.append('')

    # 1. mana_contents
    sql_lines.append('-- 1. mana_contentsテーブルに追加')
    sql_lines.append('INSERT INTO flowt_seimei.mana_contents (contents_id, name, catch, caption, category, tag_1, tag_2, tag_3, tag_4, tag_5, start_date) VALUES')
    caption_escaped = data['caption'].replace("'", "''")
    sql_lines.append(f"({menu_id}, '{data['name']}', '{data['catch']}', '{caption_escaped}', {data['category']}, NULL, NULL, NULL, NULL, NULL, '{data['start_date']}');")
    sql_lines.append('')

    # 2. mana_menu
    sql_lines.append('-- 2. mana_menuテーブルに追加')
    sql_lines.append('INSERT INTO flowt_seimei.mana_menu (contents_id, menu_id, name) VALUES')
    menu_values = []
    for idx, item_name in enumerate(data['items'], 1):
        menu_id_val = menu_id * 100 + idx
        menu_values.append(f"({menu_id}, {menu_id_val}, '{item_name}')")
    sql_lines.append(',\n'.join(menu_values) + ';')
    sql_lines.append('')

    # 3. mana_result
    sql_lines.append('-- 3. mana_resultテーブルに追加')
    sql_lines.append('INSERT INTO flowt_seimei.mana_result (contents_id, menu_id, result_id, body) VALUES')
    result_values = []
    for menu_idx, (item_name, cards) in enumerate(zip(data['items'], data['cards']), 1):
        menu_id_val = menu_id * 100 + menu_idx
        for card_idx, card_num in enumerate(cards, 1):
            result_id = int(f"{menu_id}{menu_idx:02d}{card_idx:02d}")
            result_text = data['results'][(menu_id_val, card_num)]
            result_text_escaped = result_text.replace("'", "''").replace('\n', ' ')
            result_values.append(f"({menu_id}, {menu_id_val}, {result_id}, '{result_text_escaped}')")
    sql_lines.append(',\n'.join(result_values) + ';')
    sql_lines.append('')

    # 4. mana_card
    sql_lines.append('-- 4. mana_cardテーブルに追加')
    sql_lines.append('INSERT INTO flowt_seimei.mana_card (contents_id, menu_id, result_id, body) VALUES')
    card_values = []
    for menu_idx, (item_name, cards) in enumerate(zip(data['items'], data['cards']), 1):
        menu_id_val = menu_id * 100 + menu_idx
        for card_idx, card_num in enumerate(cards, 1):
            result_id = int(f"{menu_id}{menu_idx:02d}{card_idx:02d}")
            card_values.append(f"({menu_id}, {menu_id_val}, {result_id}, {card_num})")
    sql_lines.append(',\n'.join(card_values) + ';')
    sql_lines.append('')

    # 確認クエリ
    sql_lines.append('-- ' + '=' * 40)
    sql_lines.append('-- インポート完了')
    sql_lines.append('-- ' + '=' * 40)
    sql_lines.append('-- 確認クエリ:')
    sql_lines.append(f'-- SELECT * FROM mana_contents WHERE contents_id = {menu_id};')
    sql_lines.append(f'-- SELECT * FROM mana_menu WHERE contents_id = {menu_id};')
    sql_lines.append(f'-- SELECT * FROM mana_result WHERE contents_id = {menu_id};')
    sql_lines.append(f'-- SELECT * FROM mana_card WHERE contents_id = {menu_id};')
    sql_lines.append('')

    return '\n'.join(sql_lines)

